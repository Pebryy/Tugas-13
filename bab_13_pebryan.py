    def __init__(self, vertices):

        # Jumlah vertex
        self.V = vertices

        # Menyimpan seluruh edge
        self.graph = []

        # Menyimpan daftar node
        self.nodes = []

        # Menyimpan hasil Minimum Spanning Tree
        self.MST = []


    # Menambahkan edge
    def addEdge(self, s, d, w):

        # s = source
        # d = destination
        # w = weight
        self.graph.append([s, d, w])


    # Menambahkan node
    def addNode(self, value):

        self.nodes.append(value)


    # Menampilkan hasil MST
    def printSolution(self, s, d, w):

        print("Minimum Spanning Tree :")

        for s, d, w in self.MST:
            print("%s - %s : %s" % (s, d, w))


    # Algoritma Kruskal
    def kruskalAlgo(self):

        # i = index edge
        # e = jumlah edge yang dipilih
        i, e = 0, 0

        # Membuat Disjoint Set
        ds = DisjointSet(self.nodes)

        # Mengurutkan edge berdasarkan bobot
        self.graph = sorted(
            self.graph,
            key=lambda item: item[2]
        )

        # Memilih edge hingga MST berisi V-1 edge
        while e < self.V - 1:

            s, d, w = self.graph[i]
            i += 1

            x = ds.find(s)
            y = ds.find(d)

            # Jika tidak membentuk siklus
            if x != y:

                e += 1

                self.MST.append([s, d, w])

                ds.union(x, y)

        self.printSolution(s, d, w)


