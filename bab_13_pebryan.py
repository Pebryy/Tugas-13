class Graph:

    # Constructor
    def __init__(self, vertices):

        # Jumlah vertex
        self.V = vertices

        # Menyimpan seluruh edge
        self.graph = []

        # Menyimpan daftar node
        self.nodes = []


    # Menambahkan edge beserta bobotnya
    def add_edge(self, s, d, w):

        # s = source
        # d = destination
        # w = weight
        self.graph.append([s, d, w])


    # Menambahkan node
    def addNode(self, value):

        self.nodes.append(value)


    # Menampilkan hasil jarak terpendek
    def print_solution(self, dist):

        print("Vertex Distance from Source")

        for key, value in dist.items():
            print(" ", key, " : ", value)


    # Algoritma Bellman-Ford
    def bellmanFord(self, src):

        # Inisialisasi semua jarak = tak hingga
        dist = {
            i: float("Inf")
            for i in self.nodes
        }

        # Jarak node awal = 0
        dist[src] = 0

        # Relaksasi seluruh edge sebanyak V-1 kali
        for _ in range(self.V - 1):

            for s, d, w in self.graph:

                if (dist[s] != float("Inf") and
                        dist[s] + w < dist[d]):

                    dist[d] = dist[s] + w

        # Mengecek adanya negative cycle
        for s, d, w in self.graph:

            if (dist[s] != float("Inf") and
                    dist[s] + w < dist[d]):

                print("Graph contains negative cycle")
                return

        # Menampilkan hasil
        self.print_solution(dist)


