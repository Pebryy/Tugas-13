# Prim's Algorithm in Python
# Algoritma Prim digunakan untuk mencari Minimum Spanning Tree (MST),
# yaitu kumpulan sisi dengan total bobot minimum yang menghubungkan
# seluruh simpul pada graph tanpa membentuk siklus.

import sys

class Graph:
    def __init__(self, vertexNum, edges, nodes):
        # Matriks bobot antar simpul
        self.edges = edges

        # Daftar nama simpul
        self.nodes = nodes

        # Jumlah simpul
        self.vertexNum = vertexNum

        # Menyimpan hasil Minimum Spanning Tree (MST)
        self.MST = []

    def printSolution(self):
        # Menampilkan hasil MST
        print("Edge : Weight")
        for s, d, w in self.MST:
            print("%s -> %s: %s" % (s, d, w))

    def primsAlgo(self):
        # Menandai simpul yang sudah dikunjungi
        visited = [0] * self.vertexNum

        # Menghitung jumlah edge yang sudah masuk ke MST
        edgeNum = 0

        # Memulai dari simpul pertama
        visited[0] = True

        # Ulangi sampai semua simpul terhubung
        while edgeNum < self.vertexNum - 1:
            min = sys.maxsize

            # Mencari edge dengan bobot terkecil
            # yang menghubungkan simpul yang sudah dikunjungi
            # ke simpul yang belum dikunjungi
            for i in range(self.vertexNum):
                if visited[i]:
                    for j in range(self.vertexNum):
                        if (not visited[j]) and self.edges[i][j]:
                            if min > self.edges[i][j]:
                                min = self.edges[i][j]
                                s = i
                                d = j

            # Menambahkan edge ke MST
            self.MST.append([self.nodes[s], self.nodes[d], self.edges[s][d]])

            # Tandai simpul tujuan sebagai sudah dikunjungi
            visited[d] = True

            # Tambah jumlah edge yang sudah dipilih
            edgeNum += 1

        # Menampilkan hasil MST
        self.printSolution()


