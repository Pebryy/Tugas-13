# Searching Algorithms - Linear Search
# Linear Search mencari data dengan memeriksa setiap elemen
# secara berurutan dari awal hingga akhir list.

def linearSearch(array, value):
    # Melakukan perulangan pada seluruh elemen array
    for i in range(len(array)):
        # Jika elemen yang dicari ditemukan
        if array[i] == value:
            # Mengembalikan indeks elemen tersebut
            return i
    # Jika data tidak ditemukan, mengembalikan -1
    return -1


