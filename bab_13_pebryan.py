# Dijkstra Algorithm (Dictionary Version)

from collections import defaultdict


# ==========================
# CLASS GRAPH
# ==========================

class Graph:

    def __init__(self):

        # Menyimpan seluruh node
        self.nodes = set()

        # Adjacency List
        self.edges = defaultdict(list)

        # Menyimpan bobot setiap edge
        self.distances = {}


    # Menambahkan node
    def addNode(self, value):

        self.nodes.add(value)


    # Menambahkan edge beserta bobot
    def addEdge(self, fromNode, toNode, distance):

        self.edges[fromNode].append(toNode)
        self.distances[(fromNode, toNode)] = distance


# ==========================
# DIJKSTRA ALGORITHM
# ==========================

def dijkstra(graph, initial):

    # Menyimpan jarak terpendek
    visited = {initial: 0}

    # Menyimpan predecessor (jalur)
    path = defaultdict(list)

    # Salinan seluruh node
    nodes = set(graph.nodes)

    while nodes:

        # Mencari node dengan jarak terkecil
        minNode = None

        for node in nodes:

            if node in visited:

                if minNode is None:
                    minNode = node

                elif visited[node] < visited[minNode]:
                    minNode = node

        if minNode is None:
            break

        nodes.remove(minNode)

        currentWeight = visited[minNode]

        # Relaksasi seluruh tetangga
        for edge in graph.edges[minNode]:

            weight = currentWeight + graph.distances[(minNode, edge)]

            if edge not in visited or weight < visited[edge]:

                visited[edge] = weight

                # Menyimpan predecessor
                path[edge] = [minNode]

    return visited, path


