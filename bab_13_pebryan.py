# Disjoint Set (Union-Find)

class DisjointSet:

    # Constructor
    def __init__(self, vertices):

        # Menyimpan daftar vertex
        self.vertices = vertices

        # Menyimpan parent setiap vertex
        self.parent = {}

        # Awalnya setiap vertex adalah parent dirinya sendiri
        for v in vertices:
            self.parent[v] = v

        # Menyimpan rank (tinggi pohon)
        self.rank = dict.fromkeys(vertices, 0)


    # Mencari root (representative) suatu himpunan
    def find(self, item):

        if self.parent[item] == item:
            return item
        else:
            return self.find(self.parent[item])


    # Menggabungkan dua himpunan
    def union(self, x, y):

        # Mencari root masing-masing
        xroot = self.find(x)
        yroot = self.find(y)

        # Union by Rank
        if self.rank[xroot] < self.rank[yroot]:

            self.parent[xroot] = yroot

        elif self.rank[xroot] > self.rank[yroot]:

            self.parent[yroot] = xroot

        else:

            self.parent[yroot] = xroot
            self.rank[xroot] += 1


