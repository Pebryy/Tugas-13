# Fungsi Binary Search
def binarySearch(array, value):

    # Index awal
    start = 0

    # Index akhir
    end = len(array) - 1

    # Menentukan posisi tengah
    middle = math.floor((start + end) / 2)

    # Melakukan pencarian selama data belum ditemukan
    # dan ruang pencarian masih tersedia
    while not (array[middle] == value) and start <= end:

        # Jika nilai yang dicari lebih kecil
        if value < array[middle]:

            # Geser batas kanan
            end = middle - 1

        else:
            # Geser batas kiri
            start = middle + 1

        # Menghitung kembali posisi tengah
        middle = math.floor((start + end) / 2)

        # Untuk melihat proses pencarian
        # print(start, middle, end)

    # Jika data ditemukan
    if array[middle] == value:
        return middle

    # Jika data tidak ditemukan
    else:
        return -1

