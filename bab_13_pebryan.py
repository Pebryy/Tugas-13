# ==========================

class Dijkstra:

    def __init__(self):

        # Priority Queue
        self.heap = []


    # Menghitung shortest path
    def calculate(self, start_vertex):

        # Jarak node awal = 0
        start_vertex.min_distance = 0

        # Masukkan ke priority queue
        heapq.heappush(self.heap, start_vertex)

        while self.heap:

            # Mengambil node dengan jarak terkecil
            actual_vertex = heapq.heappop(self.heap)

            if actual_vertex.visited:
                continue

            # Memeriksa seluruh tetangga
            for edge in actual_vertex.neighbors:

                start = edge.start_vertex
                target = edge.target_vertex

                # Menghitung jarak baru
                new_distance = start.min_distance + edge.weight

                # Jika lebih kecil, update jarak
                if new_distance < target.min_distance:

                    target.min_distance = new_distance
                    target.predecessor = start

                    # Masukkan kembali ke priority queue
                    heapq.heappush(self.heap, target)

            actual_vertex.visited = True


    # Menampilkan hasil shortest path
    def get_shortest_path(self, vertex):

        print("The shortest path distance :", vertex.min_distance)

        actual_vertex = vertex

        while actual_vertex is not None:
            print(actual_vertex.name, end=" ")
            actual_vertex = actual_vertex.predecessor


