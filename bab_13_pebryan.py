# ==========================
# FLOYD-WARSHALL
# ==========================

def floydWarshall(nV, G):

    # Matriks jarak awal
    distance = G

    # Memilih setiap vertex sebagai perantara
    for k in range(nV):

        # Vertex asal
        for i in range(nV):

            # Vertex tujuan
            for j in range(nV):

                # Memilih jarak yang lebih pendek
                distance[i][j] = min(
                    distance[i][j],
                    distance[i][k] + distance[k][j]
                )

    # Menampilkan hasil
    printSolution(nV, distance)


