# Breadth First Search (BFS) untuk mencari jalur (path)
# BFS menelusuri graph secara melebar (level per level)
# hingga menemukan simpul tujuan.

class Graph:
    def __init__(self, gdict=None):
        # Menyimpan graph dalam bentuk adjacency list
        if gdict is None:
            gdict = {}
        self.gdict = gdict

    def bfs(self, start, end):
        # Queue menyimpan jalur (path), bukan hanya simpul
        queue = []

        # Memulai pencarian dari node awal
        queue.append([start])

        # Selama queue masih berisi jalur
        while queue:
            # Mengambil jalur pertama (FIFO)
            path = queue.pop(0)

            # Mengambil node terakhir pada jalur
            node = path[-1]

            # Jika node tujuan ditemukan, kembalikan jalurnya
            if node == end:
                return path

            # Menambahkan seluruh tetangga ke queue
            for adjacent in self.gdict.get(node, []):
                new_path = list(path)      # Menyalin jalur sebelumnya
                new_path.append(adjacent)  # Menambahkan node tetangga
                queue.append(new_path)

        # Jika tujuan tidak ditemukan
        return None


