"""Soal 3 – Minimum Spanning Tree (Prim/Kruskal)"""

import heapq

# ==========================================
# Data Graf
# ==========================================

vertices = ['A','B','C','D','E','F','G','H','I']

edges = [
    ('A','B',4),
    ('A','H',8),
    ('B','H',11),
    ('B','C',8),
    ('C','D',7),
    ('C','I',2),
    ('C','F',4),
    ('D','E',9),
    ('D','F',14),
    ('E','F',10),
    ('F','G',2),
    ('G','H',1),
    ('G','I',6),
    ('H','I',7)
]

# ==========================================
# Membuat Adjacency List
# ==========================================

graph = {}

for v in vertices:
    graph[v] = []

for u, v, w in edges:
    graph[u].append((v, w))
    graph[v].append((u, w))


# ==========================================
# Algoritma Prim
# ==========================================

def prim(graph, start):

    visited = set()

    pq = []

    mst = []

    total_cost = 0

    visited.add(start)

    for neighbor, weight in graph[start]:
        heapq.heappush(pq, (weight, start, neighbor))

    while pq:

        weight, u, v = heapq.heappop(pq)

        if v in visited:
            continue

        visited.add(v)

        mst.append((u, v, weight))

        total_cost += weight

        for next_vertex, next_weight in graph[v]:

            if next_vertex not in visited:
                heapq.heappush(
                    pq,
                    (next_weight, v, next_vertex)
                )

    return mst, total_cost


# ==========================================
# Disjoint Set (Union Find)
# ==========================================

parent = {}

rank = {}

def make_set(vertices):

    for v in vertices:
        parent[v] = v
        rank[v] = 0

def find(v):

    if parent[v] != v:
        parent[v] = find(parent[v])

    return parent[v]

def union(v1, v2):

    root1 = find(v1)
    root2 = find(v2)

    if root1 == root2:
        return

    if rank[root1] < rank[root2]:
        parent[root1] = root2

    elif rank[root1] > rank[root2]:
        parent[root2] = root1

    else:
        parent[root2] = root1
        rank[root1] += 1


# ==========================================
# Algoritma Kruskal
# ==========================================

def kruskal(vertices, edges):

    make_set(vertices)

    mst = []

    total_cost = 0

    edges = sorted(edges, key=lambda x: x[2])

    for u, v, weight in edges:

        if find(u) != find(v):

            union(u, v)

            mst.append((u, v, weight))

            total_cost += weight

    return mst, total_cost


# ==========================================
# Program Utama
# ==========================================

print("===== PRIM =====")

mst, cost = prim(graph, 'A')

for u, v, w in mst:
    print(f"{u} - {v} = {w}")

print("Total Cost =", cost)


print("\n===== KRUSKAL =====")

mst, cost = kruskal(vertices, edges)

for u, v, w in mst:
    print(f"{u} - {v} = {w}")

print("Total Cost =", cost)