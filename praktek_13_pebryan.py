from collections import deque
import heapq

# ============================
# Adjacency List
# ============================
graph = {
    'A': [('B', 4), ('C', 2)],
    'B': [('A', 4), ('C', 1), ('D', 5)],
    'C': [('A', 2), ('B', 1), ('D', 8), ('E', 10)],
    'D': [('B', 5), ('C', 8), ('E', 2), ('F', 6)],
    'E': [('C', 10), ('D', 2), ('F', 3)],
    'F': [('D', 6), ('E', 3)]
}

# ============================
# Breadth First Search (BFS)
# ============================
def bfs(graph, start):
    visited = set()
    queue = deque([start])
    order = []

    visited.add(start)

    while queue:
        current = queue.popleft()
        order.append(current)

        for neighbor, weight in graph[current]:
            if neighbor not in visited:
                visited.add(neighbor)
                queue.append(neighbor)

    return order


# ============================
# Dijkstra
# ============================
def dijkstra(graph, start):
    distance = {node: float('inf') for node in graph}
    previous = {node: None for node in graph}

    distance[start] = 0

    pq = [(0, start)]

    while pq:
        current_distance, current_node = heapq.heappop(pq)

        if current_distance > distance[current_node]:
            continue

        for neighbor, weight in graph[current_node]:
            new_distance = current_distance + weight

            if new_distance < distance[neighbor]:
                distance[neighbor] = new_distance
                previous[neighbor] = current_node
                heapq.heappush(pq, (new_distance, neighbor))

    return distance, previous


# ============================
# Menampilkan Path
# ============================
def shortest_path(previous, start, target):
    path = []

    while target is not None:
        path.append(target)
        target = previous[target]

    path.reverse()

    return path


# ============================
# Program Utama
# ============================

print("===== BFS Traversal =====")
order = bfs(graph, 'A')
print(" -> ".join(order))

distance, previous = dijkstra(graph, 'A')

print("\n===== Shortest Distance =====")
for node in sorted(distance):
    print(f"A -> {node} = {distance[node]}")

print("\n===== Shortest Path A ke F =====")
path = shortest_path(previous, 'A', 'F')
print(" -> ".join(path))
print("Total Distance =", distance['F'])

